package com.hz.ui.myphoto.bean;

public class CatalogInfoBean {
	private String classid;  //子目录id
	private String classname; //目录名称
	private String bclassid; //父目录id
	public String getClassid() {
		return classid;
	}
	public void setClassid(String classid) {
		this.classid = classid;
	}
	public String getClassname() {
		return classname;
	}
	public void setClassname(String classname) {
		this.classname = classname;
	}
	public String getBclassid() {
		return bclassid;
	}
	public void setBclassid(String bclassid) {
		this.bclassid = bclassid;
	}
	
	

}
